    // Function to submit the login form
    function resent_otp() {
        $('#loginverify').val(0)
        var form = $('#loginForm');
        // Submit the form
        form.submit();
    }
    $("#loginForm").on('submit', function(e) {
        e.preventDefault();
        var phone = $('#loginPhone').val()
        var verify = $('#loginverify').val()
        if (verify == 0) {
            $.ajax({
                type: "POST",
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                url: "{{ route('login_process') }}",
                data: {
                    phone: phone
                },
                dataType: "json",
                success: function(response) {
                    if (response.status == true) {
                        $('.hidden-OTP-field').css('display', 'inline')
                        $('#loginPhone').attr('readonly', 'readonly')
                        $('#loginOTP').attr('required', 'required')
                        $('#loginverify').val(1)
                        notifySuccess(response.message)
                        // Set the timer duration to 2 minutes (120 seconds)
                        var duration = 120;
                        var timerDisplay = document.getElementById('timer');
                        // Update the timer every second
                        var timer = setInterval(function() {
                            var minutes = parseInt(duration / 60, 10);
                            var seconds = parseInt(duration % 60, 10);
                            // Display the remaining time
                            timerDisplay.innerHTML = '<span style="font-size:15px;font-weight: 500;margin-top:5px">Resend OTP in: ' + minutes + 'm ' + seconds + 's</span>';
                            // If the timer reaches 0, clear the interval and reset the timer
                            if (--duration < 0) {
                                clearInterval(timer);
                                timerDisplay.innerHTML = '<a href="javascript:void(0)" onclick="resent_otp()" style="font-size:15px;font-weight: 500;color:#c3545;margin-top:5px">Resend OTP</a>';
                            }
                        }, 1000);
                    } else {
                        notifyError(response.message)
                    }
                }
            });
        } else if (verify == 1) {
            var otp = $('#loginOTP').val()
            $.ajax({
                type: "POST",
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                url: "{{ route('loginotp_verify') }}",
                data: {
                    phone: phone,
                    otp: otp
                },
                dataType: "json",
                success: function(response) {
                    if (response.status == true) {
                        notifySuccess(response.message)
                        // location.reload()
                        setTimeout(function() {
                            var path = "{{ url('/') }}";
                            //    location.reload(); 
                            window.location.href = path;
                        }, 3000);
                    } else {
                        notifyError(response.message)
                    }
                }
            });
        }
    });
</script>